"""Operadores Aritmeticos con Python"""

"""Operador potencia: **"""

potencia_1= 10**3
print("El resultado de mi potencia_1 es: {}".format(potencia_1))

"""Segunda forma"""
"""Usando la palabra reservada 'pow' podemos tambien elevar a una cierta potencia"""
"""Nos pide 2 valores: la base y la potencia a la que se elevar√° respectivamente"""

potencia_2= pow(20,4)
potencia_3= pow(4,5)

print("El resultado de mi potencia_2 es: {}".format(potencia_2))
print("El resultado de mi potencia_3 es: {}".format(potencia_3))