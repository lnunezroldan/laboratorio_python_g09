"""Listas en Python"""

"""Listas sort(): Ordena los elementos de una lista"""

var_1= ["SQLServer", "RDS", "MySQL", "Postresql"]
var_2= [50, 25, 1500, 3, 45, 400]

var_1.sort()
var_2.sort()

print("Mi primera lista ordenada es: {}".format(var_1))
print("Mi segunda lista ordenada es: {}".format(var_2))
